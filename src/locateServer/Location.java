package locateServer;

public class Location {
	float x;
	float y; 
	float store[][];
   public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
	public float[][] getStore() {
		return store;
	}
	public void setStore(float[][] store) {
		this.store = store;
	}

}
